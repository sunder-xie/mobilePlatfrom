package com.kintiger.xplatform.api.role.bo;

import java.util.Date;

import com.kintiger.xplatform.framework.bo.SearchInfo;

/**
 * 角色.
 * 
 * @author xujiakun
 * 
 */
public class Role extends SearchInfo {

	private static final long serialVersionUID = -5100230087589323253L;

	private String roleId;

	private String roleName;

	private String remark;

	private String state;

	/**
	 * 字典id item value.
	 */
	private String type;

	private Date createDate;

	/**
	 * 修改人id.
	 */
	private String modifyUser;

	private Date modifyDate;

	/**
	 * 权限岗位.
	 */
	private String stationId;

	private Long stationRoleId;

	/**
	 * 权限点id.
	 */
	private Long conpointId;

	/**
	 * 职位岗位.
	 */
	private String positionTypeId;

	private Long pyRoleId;

	/**
	 * 通用id 储存 stationId/positionTypeId.
	 */
	private String id;

	/**
	 * 菜单id.
	 */
	private Long menuId;

	private String userId;

	private String passport;

	private String userName;

	private int menuCount;

	private int stationCount;

	/**
	 * dict.
	 */
	private String itemName;

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Date getCreateDate() {
		return createDate != null ? (Date) createDate.clone() : null;
	}

	public void setCreateDate(Date createDate) {
		if (createDate != null) {
			this.createDate = (Date) createDate.clone();
		}
	}

	public String getModifyUser() {
		return modifyUser;
	}

	public void setModifyUser(String modifyUser) {
		this.modifyUser = modifyUser;
	}

	public Date getModifyDate() {
		return modifyDate != null ? (Date) modifyDate.clone() : null;
	}

	public void setModifyDate(Date modifyDate) {
		if (modifyDate != null) {
			this.modifyDate = (Date) modifyDate.clone();
		}
	}

	public String getStationId() {
		return stationId;
	}

	public void setStationId(String stationId) {
		this.stationId = stationId;
	}

	public Long getStationRoleId() {
		return stationRoleId;
	}

	public void setStationRoleId(Long stationRoleId) {
		this.stationRoleId = stationRoleId;
	}

	public Long getConpointId() {
		return conpointId;
	}

	public void setConpointId(Long conpointId) {
		this.conpointId = conpointId;
	}

	public String getPositionTypeId() {
		return positionTypeId;
	}

	public void setPositionTypeId(String positionTypeId) {
		this.positionTypeId = positionTypeId;
	}

	public Long getPyRoleId() {
		return pyRoleId;
	}

	public void setPyRoleId(Long pyRoleId) {
		this.pyRoleId = pyRoleId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Long getMenuId() {
		return menuId;
	}

	public void setMenuId(Long menuId) {
		this.menuId = menuId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getPassport() {
		return passport;
	}

	public void setPassport(String passport) {
		this.passport = passport;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public int getMenuCount() {
		return menuCount;
	}

	public void setMenuCount(int menuCount) {
		this.menuCount = menuCount;
	}

	public int getStationCount() {
		return stationCount;
	}

	public void setStationCount(int stationCount) {
		this.stationCount = stationCount;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

}
